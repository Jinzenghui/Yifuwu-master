public class RegisterCallbackInfo {

    public int result;
    public String text;

    public void setResult(int result){

        this.result = result;

    }

    public int getResult(){

        return this.result;

    }

    public void setText(String text){

        this.text = text;

    }

    public String getText(){

        return this.text;

    }
}
